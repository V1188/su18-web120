<h3>What is the CSS Flexbox?</h3>

<p>The CSS flexbox is a layout mode that provides the arrangement of elements on a page such that the elements behave predictably when the page layout needs to accommodate different screen resolutions and different display devices. The Flexbox is a one dimensional layout model almost used exclusively in responsive websites that offers space distribution and better arrangement between items in an interface and amazing alignment capabilities.</p>

 <h3>How to use CSS Flexbox?</h3>

<p>" The Flexible Box Module, a.k.a. flexbox, is all the latest and greatest at the moment. It digs into the fundamentals of the CSS box-model, providing us with a powerful and highly useful module for layout configurations. It is easily defined in our CSS and markup, and responsive adaptations are just a media query away. Think about a world where we don’t have to clear floats or utilise frameworks or masses of code to create responsive grids. Flexbox does all the dirty work. For us, it’s just a simple implementation. We define a flex container, and in it we have flex items. "

There are numerous options for defining the layout content. A few of the most popular ones are: </p>

<ol>

    <li>flex-direction : This property specifies whether the items need to be in the row and column and the order in which items need to appear.</li>
    
    <li>justify-content : With this property, we can declare how the browser distributes the available space between the items.
    </li>
    
    <li>flex-wrap : With this property we can specify whether the flex items need to jump to a new line when the space in the flex container runs out.</li>

</ol>

<h3>Flexbox Polyfill</h3>

<p>Flexbox Polyfill is the code designed to support browser differences. Polyfills are basically used in order to implant HTML5 functionality in browsers that don't natively support them.
</p>

